char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (1)
	{
		if (c > 0 || !s1[i])
		{
			s1[i] = s2[c];
			if (!s2[c])
				break ;
			c += 1;
		}
		i += 1;
	}
	return (s1);
}
